# Difference between = and == operators in Python
a = 10   # = is an assignment operator, assign value to a variable
b = 20
# == is a comparison operator, it checks if two variables are equal or not. Returns bool
print(a == b)

# ** operator in Python
# It is a binary operator
print(3 ** 2)   # ** is Exponentiation operator, 3^2

# ^ operator in python
# It is Bitwise XOR operator
# It compares each bit of two numbers
# It returns 1 if only one of the two bits is 1
c = 6       # 6 = 110
d = 3       # 3 = 001
print(c ^ d)
# the output will be 101 i.e. 5


